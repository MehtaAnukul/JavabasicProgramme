public class L_Triangle123 {
    public static void main(String args[])
    {
        int i,j;
        for(i=0;i<=5;i++)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print(i);
            }
            System.out.println("");
        }
    }
}
