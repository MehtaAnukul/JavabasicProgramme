public class RL_Triangle {
    /**
     * Created by Anukul-PC on 17-01-2018.
     */
        public static void main(String arg[])
        {
            int i,j;
            for(i=5;i>=1;i--)
            {
                for(j=1;j<=i;j++)
                {
                    System.out.print("*");
                }
                System.out.println();
            }
        }
    }


